
package funcions;

public class Operacions {
    
    
    public static int suma (int num1, int num2){
        int resultat;
        
        resultat = num1+num2;
        
        return resultat;
    }
    
    public static int multiplicacio (int num1, int num2){
        int resultat;
        
        resultat = num1*num2;
        
        return resultat;
    }
    public static int resta(int numero1, int numero2){
         int resultado;
        resultado=numero1-numero2;
        return resultado;
    }
    public static int division(int numero1, int numero2){
         int resultado;
        resultado=numero1/numero2;
        return resultado;
    }
}
