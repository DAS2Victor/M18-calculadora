
package funcions;

public class Operacions {
    
    
    public static int suma (int num1, int num2){
        int resultat;
        
        resultat = num1+num2;
        
        return resultat;
    }
    
    public static int multiplicacio (int num1, int num2){
        int resultat;
        
        resultat = num1*num2;
        
        return resultat;
    }
}
